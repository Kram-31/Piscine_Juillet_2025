int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				val;

	i = 0;
	val = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && val == 0 && i < n)
	{
		val = s1[i] - s2[i];
		i++;
	}
	return (val);
}
