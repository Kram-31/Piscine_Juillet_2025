int	ft_find_next_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 0)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
		{
			nb++;
		}
		i++;
	}
	return (1);
}

