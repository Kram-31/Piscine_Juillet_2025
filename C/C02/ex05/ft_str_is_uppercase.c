#include <unistd.h>

int     ft_str_is_uppercase(char *str)
{
        int i;
        i = 0;
        while (str[i] != '\0')
        {
                if  (str[i] <= 65 && str[i] >= 90)
                {
                        write(1, "1", 1);
                        return (1);
                }
                else
                {
                        return(0);
                }
        }
	return (0);
}
