#include <unistd.h>

int	ft_is_printable(char *str)
{
        int i;
        i = 0;
        while (str[i] != '\0')
        {
                if  (str[i] <= 32 && str[i] >= 126)
                {
                        write(1, "1", 1);
                        return (1);
                }
                else
                {
                        return(0);
                }

	}
	return(0);
}
